// TestGenerateDefaultsGoVersionWhenOmitted guards against emitting an
// invalid go.mod ("go \n" with no version) when a script has no //gorn:go
// directive. GoVersion is optional at parse time, so Generate must supply
// a usable default.
func TestGenerateDefaultsGoVersionWhenOmitted(t *testing.T) {
	source := []byte("//gorn:main\n" +
		"println(\"hello\")\n")

	script, err := gornparser.ParseSource("no-go-version.gorn", source)
	if err != nil {
		t.Fatal(err)
	}
	if script.GoVersion != "" {
		t.Fatalf("GoVersion = %q, want empty (test assumes //gorn:go is optional)", script.GoVersion)
	}

	generated, err := gornparser.Generate(script)
	if err != nil {
		t.Fatal(err)
	}

	mod := string(generated.ModGenerated)
	if strings.Contains(mod, "go \n") || strings.Contains(mod, "go\n") {
		t.Fatalf("ModGenerated has no go version:\n%s", mod)
	}
	if !strings.Contains(mod, "go 1.26\n") {
		t.Fatalf("ModGenerated missing expected default go version:\n%s", mod)
	}
}

func TestGenerateReturnsRawMainWhenFormattingFails(t *testing.T) {
	source := []byte("//gorn:go 1.26\n" +
		"//gorn:main\n" +
		"if {\n")

	script, err := gornparser.ParseSource("bad-go.gorn", source)
	if err != nil {
		t.Fatal(err)
	}

	generated, err := gornparser.Generate(script)
	if generated != nil {
		t.Fatalf("Generated = %+v, want nil on format failure", generated)
	}
	var genErr *gornparser.GenerateError
	if !errors.As(err, &genErr) {
		t.Fatalf("error type = %T, want *gornparser.GenerateError", err)
	}
	if len(genErr.Raw) == 0 {
		t.Fatal("GenerateError.Raw is empty, want raw generated source")
	}
}

func TestGenerateRejectsPreambleImportConflict(t *testing.T) {
	tests := []struct {
		name     string
		source   string
		wantLine int
	}{
		{
			name: "single import",
			source: "//gorn:preamble\n" + // 1
				"\n" + // 2
				"import \"strings\"\n" + // 3
				"//gorn:main\n" + // 4
				"strings.ToUpper(\"x\")\n", // 5
			wantLine: 3,
		},
		{
			name: "import block",
			source: "//gorn:preamble\n" + // 1
				"import (\n" + // 2
				"\t\"fmt\"\n" + // 3
				"\t\"strings\"\n" + // 4
				")\n" + // 5
				"//gorn:main\n" + // 6
				"fmt.Println()\n", // 7
			wantLine: 3,
		},
		{
			name: "aliased import still conflicts by path",
			source: "//gorn:preamble\n" + // 1
				"import f \"fmt\"\n" + // 2
				"//gorn:main\n" + // 3
				"f.Println()\n", // 4
			wantLine: 2,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			script, err := gornparser.ParseSource("conflict.gorn", []byte(tt.source))
			if err != nil {
				t.Fatal(err)
			}
			_, err = gornparser.Generate(script)
			if !errors.Is(err, gornparser.ErrPreambleImportConflict) {
				t.Fatalf("error = %v, want errors.Is(_, %v)", err, gornparser.ErrPreambleImportConflict)
			}
			var parseErr *gornparser.Error
			if !errors.As(err, &parseErr) {
				t.Fatalf("error type = %T, want *gornparser.Error", err)
			}
			if parseErr.Line != tt.wantLine {
				t.Fatalf("error line = %d, want %d", parseErr.Line, tt.wantLine)
			}
		})
	}
}

