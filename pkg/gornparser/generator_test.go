func TestGenerateProducesModuleAndFormattedMain(t *testing.T) {
	source := []byte("//gorn:go 1.26\n" +
		"//gorn:module example.com/scripts/hello\n" +
		"//gorn:require github.com/example/tool v1.2.3\n" +
		"\n" +
		"import \"fmt\"\n" +
		"\n" +
		"//gorn:main\n" +
		"\n" +
		"fmt.Println(\"hello\")\n" +
		"sh.Host()\n")

	script, err := gornparser.ParseSource("hello.gorn", source)
	if err != nil {
		t.Fatal(err)
	}

	generated, err := gornparser.Generate(script)
	if err != nil {
		t.Fatal(err)
	}

	wantMod := "module example.com/scripts/hello\n\n" +
		"go 1.26\n" +
		"require (\n" +
		"\tgithub.com/example/tool v1.2.3\n" +
		")\n"
	if got := string(generated.ModGenerated); got != wantMod {
		t.Fatalf("ModGenerated = %q, want %q", got, wantMod)
	}

	mainFile := string(generated.MainFileFormatted)
	for _, want := range []string{
		"// Code generated by gorn from hello.gorn; DO NOT EDIT.",
		"package main",
		"import \"fmt\"",
		"func main() {",
		"\tfmt.Println(\"hello\")",
		"\tsh.Host()",
	} {
		if !strings.Contains(mainFile, want) {
			t.Fatalf("formatted main missing %q:\n%s", want, mainFile)
		}
	}
	if strings.Contains(mainFile, "gorn.dev/gorn/sh") {
		t.Fatalf("formatted main uses stale sh import path:\n%s", mainFile)
	}
}

