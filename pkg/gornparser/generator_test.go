// TestGeneratePreambleOutputCompiles is the real guard against generated
// output that gofmt accepts but the Go compiler rejects (e.g. an undefined
// keep-alive symbol, or an unused import). It builds a preamble script that
// references none of the preamble packages directly, in a temp module that
// replaces gorn with this working tree.
func TestGeneratePreambleOutputCompiles(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping go build in short mode")
	}

	source := []byte("//gorn:go 1.26\n" +
		"//gorn:preamble\n" +
		"//gorn:main\n" +
		"println(\"no preamble packages used directly\")\n")

	script, err := gornparser.ParseSource("compile.gorn", source)
	if err != nil {
		t.Fatal(err)
	}
	generated, err := gornparser.Generate(script)
	if err != nil {
		t.Fatal(err)
	}

	repoRoot, err := filepath.Abs("../..")
	if err != nil {
		t.Fatal(err)
	}

	dir := t.TempDir()
	goMod := string(generated.ModGenerated) +
		"\nrequire github.com/gornkit/gorn v0.0.0\n" +
		"replace github.com/gornkit/gorn => " + repoRoot + "\n"
	writeFile(t, filepath.Join(dir, "go.mod"), goMod)
	writeFile(t, filepath.Join(dir, "main.go"), string(generated.MainFileFormatted))

	runGo(t, dir, "mod", "tidy")
	runGo(t, dir, "build", "./...")
}

func writeFile(t *testing.T, path, content string) {
	t.Helper()
	if err := os.WriteFile(path, []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}
}

func runGo(t *testing.T, dir string, args ...string) {
	t.Helper()
	cmd := exec.Command("go", args...) //nolint:gosec // args are test-controlled literals
	cmd.Dir = dir
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("go %s failed: %v\n%s", strings.Join(args, " "), err, out)
	}
}
